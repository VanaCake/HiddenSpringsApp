package com.example.hiddenspringsapp;

public class FriyayGamesItem {
    private String game;
    private String players;

    public FriyayGamesItem(String game, String players) {
        this.game = game;
        this.players = players;
    }

    public String getGame() {
        return game;
    }

    public String getPlayers() {
        return players;
    }
}
